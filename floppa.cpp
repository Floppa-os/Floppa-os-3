#include <iostream>  // Подключаем библиотеку для работы с вводом и выводом (cin, cout)

using namespace std;  // Используем стандартное пространство имён, чтобы не писать std:: перед функциями

int main() {
    char operation;      // Переменная для хранения выбранного арифметического оператора (+, -, *, /)
    double num1, num2;  // Переменные для хранения двух чисел, с которыми будет проводиться операция

    // Выводим приветственное сообщение и запрашиваем у пользователя оператор
    cout << "Простой калькулятор на C++" << endl;
    cout << "Введите оператор (+, -, *, /): ";
    cin >> operation;  // Считываем оператор, введённый пользователем

    // Запрашиваем два числа для вычисления
    cout << "Введите два числа: ";
    cin >> num1 >> num2;  // Считываем два числа, разделённых пробелом

    cout << endl << "Результат: " << endl;

    // Используем конструкцию switch-case для выбора нужной операции в зависимости от введённого оператора
    switch (operation) {
        case '+':  // Если оператор — сложение
            cout << num1 << " + " << num2 << " = " << num1 + num2 << endl;
            break;  // Завершаем выполнение switch после выполнения операции

        case '-':  // Если оператор — вычитание
            cout << num1 << " - " << num2 << " = " << num1 - num2 << endl;
            break;

        case '*':  // Если оператор — умножение
            cout << num1 << " * " << num2 << " = " << num1 * num2 << endl;
            break;

        case '/':  // Если оператор — деление
            // Проверяем, не пытается ли пользователь разделить на ноль
            if (num2 != 0) {
                cout << num1 << " / " << num2 << " = " << num1 / num2 << endl;
            } else {
                cout << "Ошибка: деление на ноль невозможно! Хотя всё невозможное возможно!" << endl;
            }
            break;

        default:  // Если введённый оператор не соответствует ни одному из перечисленных
            cout << "Ошибка: введён неверный оператор!" << endl;
            break;
    }

    return 0;  // Возвращаем 0, что означает успешное завершение программы
}
